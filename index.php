				<h2>Start your own</h2>
					<p>Hackspedition is an open framework. Yo can use it, adapt it and contribute to it.</p>
					<p>You can follow our guidelines. Here we hand you some resources and some history that might be of some use.</p>
					<p>If you have further questions, please contact us at <a href="mailto:lets.do.it@hackspedition.org">lets.do.it@hackspedition.org</a></p>
				<h2>Basic principles</h2>
					<ul>
						<li>Hackspedition should be open and non-exclusive.</li>
						<li>There should be space and time to do some actual social hacking.</li>
						<li>There should be leisure/adventure activities that have nothing to do with computers.</li>
						<li>The event should involve at least two communities from two different geographies</li>
					</ul>
				<h2>Guidelines</h2>
					<ol>
						<li>Organize a three to five day trip. One of the communities can host the other community</li>
						<li>Don't bother to  book hotel rooms. Ideally, guest hackers can borrow a couch or mattress from host hackers.</li>
						<li>A welcome party is awesome. Reserve a small bar and go for a couple of drinks together the first night.</li>
						<li>Sightseeing is good, exploration is better. Most hackers are very curious people regardless the situation. Give them enough materials to make the experience as insightful as possible.</li>
						<li>Mix, mix, mix. The whole point of Hackspeditions is to cross-pollinate. Do whatever is necessary to beat language or interpersonal barriers.</li>
						<li>Mind about cultural differences. Take some time to discuss the agenda with people from both communities.</li>
						<li>Hack together. Try to have a SuperHappyDevHouse or similar event built into the event.</li>
						<li>Push your limits. People really get fond of each other when they live intense experiences together. Adventure activities such as rafting, hiking or canoeing are perfect to achieve this.</li>
						<li>Run a wrap up session. Make sure everybody is aware of the achievements made during the Hackspedition. Also make sure people have the means to stay in touch.</li>
						<li>Do. Learn. Share. Iterate.</li>
					</ol>
